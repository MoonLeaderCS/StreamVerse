import { createError } from "../error.js";
import User from "../models/user.js";
import Video from "../models/Video.js";

export const createVideo = async (req, res, next) => {
  const newVideo = new Video({ userId: req.user.id, ...req.body });
  try {
    const savedVideo = await newVideo.save();
    res.status(200).json(savedVideo);
  } catch (err) {
    next(err);
  }
};

export const updateVideo = async (req, res, next) => {
  try {
    const video = await Video.findById(req.params.id);
    if (!video) return next(createError(404, "Error: Video not found!"));

    if (req.user.id === video.userId) {
      const updatedVideo = await Video.findByIdAndUpdate(
        req.params.id,
        {
          $set: req.body,
        },
        { new: true }
      );
      res.status(200).json(updatedVideo);
    } else {
      return next(
        createError(403, "You are not permitted to update this video!")
      );
    }
  } catch (err) {
    next(err);
  }
};

export const deleteVideo = async (req, res, next) => {
  try {
    const video = await Video.findById(req.params.id);
    if (!video) return next(createError(404, "Error: Video not found!"));

    if (req.user.id === video.userId) {
      await Video.findByIdAndDelete(req.params.id);
      res
        .status(200)
        .json("Successfully... [" + video.title + "] video has been deleted!");
    } else {
      return next(
        createError(403, "You are not permitted to delete this video!")
      );
    }
  } catch (err) {
    next(err);
  }
};

